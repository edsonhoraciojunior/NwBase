namespace NwBase\Filter;

use Zend\Filter\FilterInterface;

/**
 * Remove todos as acentuações
 * 
 * @category NwBase
 * @package  NwBase\Filter
 * @author   Renato Moura <renato@naturalweb.com.br>
 */
class Transliteration implements FilterInterface
{
    /**
     * Metodo Filtra
     *
     * @return string
     */
    public function filter($string)
    {
        $replace_pairs = array(
            'À' => 'A',
            'Á' => 'A',
            'Â' => 'A',
            'Ã' => 'A',
            'Ä' => 'A',
            'Å' => 'A',
            'Æ' => 'A',
            'Ç' => 'C',
            'È' => 'E',
            'É' => 'E',
            'Ê' => 'E',
            'Ë' => 'E',
            'Ì' => 'I',
            'Í' => 'I',
            'Î' => 'I',
            'Ï' => 'I',
            'Ð' => 'D',
            'Ñ' => 'N',
            'Ò' => 'O',
            'Ó' => 'O',
            'Ô' => 'O',
            'Õ' => 'O',
            'Ö' => 'O',
            'Ø' => 'O',
            'Ù' => 'U',
            'Ú' => 'U',
            'Û' => 'U',
            'Ü' => 'U',
            'Ý' => 'Y',
            'Þ' => 'B',
            '$' => 'S',
            'ß' => 'B',
            'à' => 'a',
            'á' => 'a',
            'â' => 'a',
            'ã' => 'a',
            'ä' => 'a',
            'å' => 'a',
            'æ' => 'a',
            'ç' => 'c',
            'è' => 'e',
            'é' => 'e',
            'ê' => 'e',
            'ë' => 'e',
            'ì' => 'i',
            'í' => 'i',
            'î' => 'i',
            'ï' => 'i',
            'ð' => 'd',
            'ñ' => 'n',
            'ò' => 'o',
            'ó' => 'o',
            'ô' => 'o',
            'õ' => 'o',
            'ö' => 'o',
            'ø' => 'o',
            'ù' => 'u',
            'ú' => 'u',
            'û' => 'u',
            'ü' => 'u',
            'ý' => 'Y',
            'ý' => 'Y',
            'þ' => 'B',
            'ÿ' => 'Y',
            'Ŕ' => 'R',
            'ŕ' => 'r',
            '°' => 'o',
            'º' => 'o',
            'ª' => 'a',
        );
        
        // Efetua a substituição
        $string = strtr($string, $replace_pairs);
        
        return $string;
    }
}
